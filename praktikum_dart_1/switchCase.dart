void main(List<String> args) {
  var dayOfWeek = 5;
  switch (dayOfWeek) {
    case 1:
      print("Day is sunday");
      break;
    case 2:
      print("Day is monday");
      break;
    case 3:
      print("Day is tuesday");
      break;
    case 4:
      print("Day is wednesday");
      break;
    case 5:
      print("Day is thursday");
      break;
    case 6:
      print("Day is friday");
      break;
    case 7:
      print("Day is saturday");
      break;
    default:
      print("Invalid day");
      break;
  }
}
